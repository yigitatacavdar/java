package javaweb;

public class ifelse{
	
	public static void main(String[] args) {
		int number = 10;
		int number2 = 20;
		
		if(number < number2) {
			System.out.println("number 2 is smaller than number");
		}
		else if(number == number2) {
			System.out.println("numbers are equal");
		}
		else {
			System.out.println("number2 is bigger than number");
		}
	}
}